                                    <h4>Return to a ’Kinder and Gentler Nation’</h4>
                                    <p>Is it any surprise that Congress ranked at the bottom of the most recent Gallup poll of institutions Americans feel confidence in? Such poll results are a sad reflection of voters’ impatience with the lack of compromise and increasing polarization in Washington. “This whole thing that’s happened over the last 20-plus years is somewhat alien to those of us who grew up in a different time,” says former President Bill Clinton. “You fight in the election. You have your differences. The voters would decide, and then they expected you to get something done. And now it’s viewed as something rare.”</p>
                                    <p>“If you’re fighting with somebody all the time … you literally can’t hear what the other person’s saying,” Clinton adds. “If you’re in constant combat mode – there can be nothing good about your opponent and nothing bad about you and you’re never wrong and they’re never right – it makes you deaf. It shuts your brain down. And that’s a bad thing.”</p>
                                    <p>But the effects go beyond the White House and Congress to the American people. “Hatred corrodes the container it’s in,” says former Wyoming Republican Sen. Alan Simpson. “And don’t think it ain’t out there. [People] hate Congress. They hate the president. They hate the leaders.” Simpson makes a great point: “You can’t hate politicians and love democracy.”</p>
