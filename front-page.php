                                    <h4>Time to Lower the Drinking Age</h4>
                                    <p>I was telling my college-age daughter recently that back in the olden days when I went to college, you could fill a red Solo cup with beer at a fraternity party and sip it all night long. No one knew if it was your first beer or your 10th. There was no need for “pregaming” – binge drinking in private apartments or dorms before heading out in public. And unlike today, college kids didn’t tend to use fake IDs as much</p>
                                    <p>That’s because when I was an undergrad, the drinking age was 18. Fraternities had kegs out in the open on university property, and student gatherings on campus often included beer. I remember university police regularly strolling through the fraternity parties, making sure everything was under control. That tended to keep a lid on things.</p>
                                    <p>Then, 30 years ago this summer, President Ronald Reagan signed into law the Federal Uniform Drinking Age Act of 1984, which mandated that all states adopt 21 as the legal drinking age over the next five years. States that did not comply faced a cut in their federal highway funds; by 1988, all 50 states had moved the minimum drinking age to 21.</p>   
