                                    <h4>Why I Am Done Defending Herman Cain</h4>
                                    <p>People have been asking me all week if conservatives will stick with Herman Cain, but I think he's got a bigger problem than the conservative vote. A lot more women than conservatives vote, and the women I've talked to are finished with him.</p>
                                    <p>There's a new CBS poll which says that 38 percent of female Republican primary voters are "less likely to back him" now that more accusers have come forward. Among all registered voters, CBS reports that Cain has lost support among women since last month—from 28 percent in October to 15 percent now. My sense is that 15 percent of female registered voters remaining as Cain supporters may be a little high, because the poll was conducted before Cain's attorney started threatening any other potential accusers with the notion that they'd better "think twice" before coming forward.</p>
