                                    <h4>Asking Voters for ID Is Not a Race Issue</h4>
                                    <p>So I was sick last week and went to the emergency room, where I had to show ID before I was seen by a doctor; he thought it might be appendicitis and sent me for a CT scan. Again, I had to show ID before being scanned. (By the way, I’m fine, just a bad stomach bug.) As I think about it, over the last month I’ve had to show ID to rent a car, fly on a plane, and get a hotel room. I’ve been asked for ID at the grocery store in order to buy a six-pack of beer, at office buildings in Washington so I could get past the lobby security guard, and at the bank to get a cash withdrawal. We all know what a hassle it is to have your wallet stolen—it’s not that the canceling of the credit cards is so bad, it’s the losing of the ID that makes it a crisis. These days, you have to show your ID for just about everything.</p>
                                    <p>That’s what makes E.J. Dionne’s column this week so mystifying. Dionne wrote about the push in many states to require ID before one can vote. He points out that in Texas, for example, “The law allows concealed handgun licenses as identification, but not student IDs.”  Maybe that has something to do with the fact that so many student IDs are altered and used as fake IDs to buy beer; back when I was in college, most bars wouldn’t accept a student ID, only a government-issued drivers’ license, as proof of age. Handgun licenses are government-issued as well, which would explain why a state government would approve their use, but not that of student IDs. But Dionne tries to make it into a partisan issue by arguing that Sen. John McCain won a wider margin of gun-owning voters nationally than Barack Obama did, and that really, Republicans in state legislatures are behind all of this “rigging.”</p>
                                    <p class="learn-more"><div class="button"><a href="http://www.usnews.com/opinion/blogs/mary-kate-cary/2011/06/24/asking-voters-for-id-is-not-a-race-issue" target="_blank">Full article</a></div></p>
